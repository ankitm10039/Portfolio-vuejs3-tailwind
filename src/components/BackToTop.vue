<template>
    <Button severity="warn" v-if="isVisible" @click="scrollToTop" class="fixed bottom-4 right-4 bg-gradient-to-r from-primary to-pink-500 text-white
    rounded-full w-10 h-10 flex items-center justify-center text-xl cursor-pointer transition-opacity duration-300 hover:bg-blue-600
    " icon="pi pi-arrow-up" style="float: right; margin-top: -30px;"/>
</template>
<script setup>
import { ref,onMounted ,onUnmounted} from 'vue';

import Button from 'primevue/button';

const isVisible =ref(false);
const handleScroll = ()=>{
    isVisible.value = window.scrollY > 300;
}
const scrollToTop = ()=>{
    window.scrollTo({top:0,behavior:'smooth'})
}
onMounted(()=>{
    window.addEventListener('scroll',handleScroll)
});

onUnmounted(()=>{
    window.removeEventListener('scroll',handleScroll);
})
</script>
